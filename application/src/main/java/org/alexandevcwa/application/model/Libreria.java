package org.alexandevcwa.application.model;

public class Libreria {
    private int libId;
    private String libNombre;
    private String libDireccion;
    private String libZipcode;
    private String libEmail;
    private String libTelefono;

    public int getLibId() {
        return libId;
    }

    public void setLibId(int libId) {
        this.libId = libId;
    }

    public String getLibNombre() {
        return libNombre;
    }

    public void setLibNombre(String libNombre) {
        this.libNombre = libNombre;
    }

    public String getLibDireccion() {
        return libDireccion;
    }

    public void setLibDireccion(String libDireccion) {
        this.libDireccion = libDireccion;
    }

    public String getLibZipcode() {
        return libZipcode;
    }

    public void setLibZipcode(String libZipcode) {
        this.libZipcode = libZipcode;
    }

    public String getLibEmail() {
        return libEmail;
    }

    public void setLibEmail(String libEmail) {
        this.libEmail = libEmail;
    }

    public String getLibTelefono() {
        return libTelefono;
    }

    public void setLibTelefono(String libTelefono) {
        this.libTelefono = libTelefono;
    }
}
